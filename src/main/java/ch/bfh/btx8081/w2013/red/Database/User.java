package ch.bfh.btx8081.w2013.red.Database;


public class User {
	private String name;
	private String password;


	public User(String name, String string) {
		this.name = name;
		this.password = string;
	}


	public String getName() {
	
		return name;
	}

	public String getPasswort() {
	
		return password;
	}

}
